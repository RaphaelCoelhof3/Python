# Programa em Python que receba (entrada de dados) o valor correspondente ao lado de um quadrado, calcule e imprima (saída de dados) seu perímetro e sua área.


Lado = (input("Digite o valor correspondente ao lado de um quadrado: "))
perimetro = int(Lado) * 4
area = int(Lado) ** 2
print("perimetro:", perimetro, "area:", area)
